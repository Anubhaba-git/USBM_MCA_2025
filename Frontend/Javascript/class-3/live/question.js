// Q-1: Input no and know the daysName , using switch case.
let dayNumber = 5;
switch (dayNumber){
     case 0:
        console.log(`It's Monday`);
        break;
     case 1:
        console.log(`It's Tuesday`);
        break;
     case 2:
        console.log(`It's Wednesday`);
        break;
     case 3:
        console.log(`It's Thursday`);
        break;
     case 4:
        console.log(`It's Friday`);
        break;
     case 5:
        console.log(`It's Saturday`);
        break;
    case 6:
        console.log(`It's Sunday`);
        break;
    default:
        console.log(`Invalid dayNumber !! Enter between 0-6`);
}

